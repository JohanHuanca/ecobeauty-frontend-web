-- Tabla para almacenar la informaci√≥n recolectada del formulario N8N, vinculada a profiles.
CREATE TABLE public.profile_metrics (
  profile_id UUID PRIMARY KEY REFERENCES public.profiles(id) ON DELETE CASCADE,
  experience_level TEXT NOT NULL,
  age INT NOT NULL CHECK (age >= 18 AND age <= 100),
  gender TEXT NOT NULL,
  main_goals TEXT NOT NULL CHECK (char_length(main_goals) <= 200),
  main_challenges TEXT NOT NULL CHECK (char_length(main_challenges) <= 200),
  info_habits TEXT[] NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW()
);
COMMENT ON TABLE public.profile_metrics IS 'M√©tricas y KPIs recolectados del formulario N8N, vinculados a cada perfil.';

-- Tabla para perfiles extendidos, vinculada a los usuarios de Supabase Auth.
CREATE TABLE public.profiles (
  id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  full_name TEXT,
  avatar_url TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);
COMMENT ON TABLE public.profiles IS 'Almacena datos p√∫blicos de los perfiles de usuario.';

-- Tabla para definir los roles de la aplicaci√≥n.
CREATE TABLE public.roles (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL UNIQUE,
  description TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);
COMMENT ON TABLE public.roles IS 'Define los roles de usuario (ej. principiante, experto).';

-- Tabla de uni√≥n para asignar roles a los perfiles.
CREATE TABLE public.user_roles (
  profile_id UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  role_id BIGINT NOT NULL REFERENCES public.roles(id) ON DELETE CASCADE,
  PRIMARY KEY (profile_id, role_id)
);
COMMENT ON TABLE public.user_roles IS 'Asigna uno o m√°s roles a cada perfil de usuario.';

